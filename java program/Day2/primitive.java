class primitive
{
	public static void main(String arr[])
  {
  int a=13;
  String ch="Deepa";
  double d=123.322;
  long l=5576879L;
  char c='D';
  float f1=167.1f;
  
  
  System.out.println("Integer:"+a);
  System.out.println("String:"+ch);
  System.out.println("Double:"+d);
  System.out.println("Long:"+l);
  System.out.println("char:"+c);
  System.out.println("Float:"+f1);
  }
  
}